    int i;

    POBuffer bufLine = TMP_BUF;

    char fileNameOptimized[MAX_TEMPORARY_STORAGE];
    FILE * fileAsm;
    FILE * fileOptimized;

    sprintf( fileNameOptimized, "%s.asm", get_temporary_filename( _environment ) );
        
