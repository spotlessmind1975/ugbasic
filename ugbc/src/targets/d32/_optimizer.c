    //         }            
    //     }

    if(po_buf_match( buf[0], " * #*", op, var) && vars_ok(var) ) {
        struct var *v = vars_get(var);
        if(v->offset > 0 && strstr(var->str,"+$")==NULL) {
            optim(buf[0], "direct-page3", "\t%s #%s+$%04x", op->str, var->str, DIRECT_PAGE);
        }            
    }
    
