/**
 * @brief Emit ASM code for <b>MOVE TILE [image] AT [int],[int]</b>
 * 
 * This function outputs a code that move a tile on a tilemap. 
 * 
 * @param _environment Current calling environment
 * @param _image Image to draw
 * @param _x Abscissa of the point to draw
 * @param _y Ordinate of the point
 */
void move_tile( Environment * _environment, char * _tile, char * _x, char * _y ) {

    Variable * tile = variable_retrieve( _environment, _tile );
    Variable * x = variable_retrieve_or_define( _environment, _x, VT_POSITION, 0 );
    Variable * y = variable_retrieve_or_define( _environment, _y, VT_POSITION, 0 );

    switch( tile->type) {
        case VT_TILE:
            CRITICAL_CANNOT_MOVE_UNROLLED_TILE( _tile );
            break;
        case VT_TILES:
            ted_move_tiles( _environment, _tile, _x, _y );
            break;
    }

}