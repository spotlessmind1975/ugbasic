; Set the CPU Speed
;
; Enter with value in B
; If B=1 then set the speed at .895 Mhz
; If B=2 then set the speed at 1.79 Mhz
; If B=3 or otherwise, then set the speed at 2.864 MHz
;
CPUSPEED:
    DECB
    BEQ     CPUSPEED1
    DECB
    BEQ     CPUSPEED2
CPUSPEED3
    LDB     #$5A
    STB     $FFD9
CPUSPEED2
    STB     $FFD9
    RTS
CPUSPEED1:
    STB     $FFD8
    RTS
