CLST:

    MOV AL, [CURRENTTILEMODE]
    CMP AL, 0
    JZ CLSTDONE

    MOV AL, [CURRENTMODE]
    CMP AL, 3
    JBE CLST0
    JMP CLSTDONE

CLST0:
    MOV AL, [EMPTYTILE]
    MOV AH, [_PAPER]
    SAL AH, 1
    SAL AH, 1
    SAL AH, 1
    SAL AH, 1
    OR AH, [_PEN]
    MOV CX, 8000
    XOR DI, DI
    REP STOSW
    JP CLSTDONE

CLSTDONE:
    RET