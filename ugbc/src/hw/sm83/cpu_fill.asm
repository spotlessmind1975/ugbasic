; CPU FILL
;   Input:
;       A : pattern
;       HL : address
;       C : size
;
CPUFILL8:
    INC C
    DEC C
    RET Z
  
CPUFILL8L1:
    LD (HL), A
    DEC C
    INC HL
    JR NZ, CPUFILL8L1
    RET

CPUFILL16:
    PUSH AF
    LD A, C
    OR B
    POP AF
    RET Z
  
CPUFILL16L1:
    LD DE, HL
    INC DE
    LD (HL), A
    CALL REPLACEMENT_LDIR
    RET    