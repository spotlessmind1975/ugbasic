;*                                                                             *
;*                      SCAN CODE ROUTINE FOR COLECO                           *
;*                                                                             *
;*                             by Marco Spedaletti                             *
;*                                                                             *
;* * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * *

SCANCODE:
    LD B, 0
    LD A, 1
    OUT	($80),A
    NOP
    NOP
    NOP
    NOP
    LD	A,B
    OR	A
    JR	NZ,SCANCODE1
SCANCODE0:
    IN	A,($FC)
    CPL
    AND $0F
    JR Z, SCANCODEA
SCANCODE0B:
    NOP
    JP SCANCODEA
SCANCODE1:
    IN	A,($FF)
    CPL
    AND $0F
    JP SCANCODEA

SCANCODEA:
    RET