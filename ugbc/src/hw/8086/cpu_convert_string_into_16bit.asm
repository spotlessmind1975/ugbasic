; Routine per convertire una stringa in un numero a 16 bit
; Input:  SI = Indirizzo della stringa
;         CX = Caratteri della stringa
; Output: AX = Valore numerico convertito
; Altri registri usati: DX, BL

CONVERTSTRING16BIT:
    MOV AX, 0
    MOV DX, 10

CONVERTSTRING16BITEND:
    RET

CONVERTSTRING16BITL0:
    MOV BL, [SI]
    DEC CX
    CMP CX, 0
    JE CONVERTSTRING16BITEND

    SUB BL, '0'
    MOV BH, 0
    MUL DX

    ADD AX, BX
    INC SI
    JP CONVERTSTRING16BITL0
