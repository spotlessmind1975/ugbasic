; AL * 2^CL -> AL (signed)
CPUMUL2CONST8S:
    MOV BL, AL 
    AND BL, 0x80
    CMP BL, 0
    JZ, CPUMUL2CONST8SPOS

    XOR AL
    INC AL
CPUMUL2CONST8SPOS:
    SAL AL, CL

    CMP BL, 0
    JZ CPUMUL2CONST8SDONE

    XOR AL
    INC AL    
CPUMUL2CONST8SDONE:
    RET

CPUMUL2CONST8U:
CPUMUL2CONST8UL1:
    SAL AL, CL
    RET
