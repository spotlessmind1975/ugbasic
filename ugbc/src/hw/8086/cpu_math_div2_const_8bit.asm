; AL / 2^CL -> AL (signed)
CPUDIV2CONST8S:
    MOV BL, AL
    AND BL, 0x80
    CMP BL, 0x00
    JZ CPUDIV2CONST8SPOS

    XOR AL, 0xff
    INC AL

CPUDIV2CONST8SPOS:
    SAR AL, CL

    CMP BL, 0x0
    JZ CPUDIV2CONST8SPOSDONE

    XOR AL, 0xff
    INC AL
CPUDIV2CONST8SPOSDONE:
    RET

CPUDIV2CONST8U:
CPUDIV2CONST8UL1:
    SAR AL, CL
    RET