CPURANDOM32:
    MOV AX, [CPURANDOM_SEED]
    MOV BH, AH
    MOV BL, AL
    ADD AX, AX
    ADD AX, AX
    INC AL
    ADD AX, BX
    MOV [CPURANDOM_SEED], AX
    MOV AX, [CPURANDOM_SEED+2]
    ADD AX, AX
    CLC
    AND AL, %00101101
    XOR AL
    MOV BL, AL
    MOV [CPURANDOM_SEED+2], AX
    ADD AX, BX
    RET

CPURANDOM16:
    CALL CPURANDOM32
    RET
    