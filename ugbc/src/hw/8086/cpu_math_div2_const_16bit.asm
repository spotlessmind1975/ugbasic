; AX / 2^C -> AX (signed)
CPUDIV2CONST16S:
    MOV BX, AX
    AND BH, 0x80
    CMP BH, 0x00
    JZ CPUDIV2CONST16SPOS

    XOR AX, 0xff
    INC AX

CPUDIV2CONST16SPOS:
    SHR AX, CL

    CMP BH, 0x00
    JZ CPUDIV2CONST16SPOSDONE

    XOR AX, 0xff
    INC AX

CPUDIV2CONST16SPOSDONE:
    RET

CPUDIV2CONST16U:
CPUDIV2CONST16UL1:
    SHR AX, CL
    RET