; ; CPU FILL
; ;   Input:
; ;       A : pattern
; ;       HL : address
; ;       C : size
; ;
; CPUFILL8:
;     INC C
;     DEC C
;     RET Z
  
; CPUFILL8L1:
;     LD (HL), A
;     DEC C
;     INC HL
;     JR NZ, CPUFILL8L1
;     RET

; CPUFILL16:
;     PUSH AF
;     LD A, C
;     OR B
;     CP 0
;     JR Z, CPUFILL16Z0 
; CPUFILL16L1:
;     POP AF
;     LD DE, HL
;     INC DE
;     LD (HL), A
;     DEC BC
;     LDIR
;     RET
; CPUFILL16Z0:
;     POP AF
;     RET    
