CHAIN:
    LDA GTIAVBLIRQPREV
    STA $0222
    LDA GTIAVBLIRQPREV+1
    STA $0223
    LDA OLDIRQLISTENER
    STA $0200
    LDA OLDIRQLISTENER+1
    STA $0201
    LDA #$ff
    STA DCOMMON4
    LDA #$c0
    JSR ATARIDLOAD
    JMP CHAINEDSTART
