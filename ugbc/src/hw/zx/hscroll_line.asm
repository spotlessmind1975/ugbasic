HSCROLLLINE:
    PUSH AF
    CALL CALCULATECOLOR
    LD A, IXH
    LD IYH, A
    POP AF
    CP $80
    JP C, HSCROLLLINER
HSCROLLLINEL:
    LD A, (CONSOLEHB)
    PUSH AF
    LD A, 8
    LD (CONSOLEHB), A
    CALL HSCROLLSCREENL
    POP AF
    LD (CONSOLEHB), A
    RET
HSCROLLLINER:
    LD A, (CONSOLEHB)
    PUSH AF
    LD A, 8
    LD (CONSOLEHB), A
    CALL HSCROLLSCREENL
    POP AF
    LD (CONSOLEHB), A
    RET
